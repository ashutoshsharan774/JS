class User {
    constructor(email, password){
        this.email = email;
        this.password = password
    }

    get email(){
        return this._email.toUpperCase()
    }

    set email(value){
        this._email = value
    }
    //here we have used diff name for email because if we use the 
    //same name as in constructor then race will start between constructor and 
    //set fucntion that who will assign value to email

    get password(){
        return `${this._password}hitesh`
    }

    set password(value){
        this._password = value
    }
}

